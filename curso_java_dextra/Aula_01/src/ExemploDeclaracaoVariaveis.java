
public class ExemploDeclaracaoVariaveis {
	
	public static void main(String args[]){
		
		int valorInteiro = 1000;
		
		byte valorByte = 10;
		
		double valorDouble = 0.856;
		
		char primeiroChar = 'P';
		
		char segundoChar = 30;
		
		char terceiroChar = 'X';
		
		String meuNome = "Everton Nascimeno";
	
		
		System.out.println("Valor int:" + valorInteiro);
		System.out.println("Valor byte:" + valorByte);
		System.out.println("Valor double:" + valorDouble);
		System.out.println("Valor 1st Char:" + primeiroChar);
		System.out.println("Valor 2nd:" + segundoChar);
		System.out.println("Valor 3rd:" + terceiroChar);
		System.out.println("Resultado String:" + meuNome);
		
	}

}
